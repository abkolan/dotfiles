    delete_visual = function(state, selected_nodes)
      local function get_table_len(tbl)
        local len = 0
        for _ in pairs(tbl) do
          len = len + 1
        end
        return len
      end
      
      local count = get_table_len(selected_nodes)
      local msg = "Are you sure you want to delete " .. count .. " files ?"
      local choices = "&Yes\n&No"
      local choice = vim.fn.confirm(msg, choices)
      if choice == 1 then
        for _, node in ipairs(selected_nodes) do
          vim.fn.delete(node.path, "rf")
        end
        require("neo-tree.sources.manager").refresh(state.name)
      end
    end,
