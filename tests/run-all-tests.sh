            RUN_COVERAGE=0
            
            case "$2" in
                basic) RUN_BASIC=1 ;;
                functional) RUN_FUNCTIONAL=1 ;;
                negative) RUN_NEGATIVE=1 ;;
                workflows) RUN_WORKFLOWS=1 ;;
