    # Start JUnit XML
    cat > "$JUNIT_FILE" << EOF
<?xml version="1.0" encoding="UTF-8"?>
<testsuites name="Dotfiles Test Suite" time="0" tests="0" failures="0" errors="0">
EOF
}

close_reports() {
    echo "</testsuites>" >> "$JUNIT_FILE"
    
    # Add footer to markdown
    cat >> "$REPORT_FILE" << EOF

