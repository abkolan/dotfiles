    $0                      # Run all tests
    $0 --only functional    # Run only functional tests
    $0 --skip performance   # Run all except performance tests
    $0 --docker            # Run all tests in Docker
    $0 -v --fail-fast      # Verbose output, stop on first failure

EOF
    exit 0
}

init_report_dir() {
    mkdir -p "$REPORT_DIR"
    
    # Start markdown report
    cat > "$REPORT_FILE" << EOF
